def _gsi_env(proxy: str) -> dict:
    env = os.environ.copy()
    env["X509_CERT_DIR"]   = CA_DIR
    env["X509_USER_PROXY"] = proxy
    env["XrdSecPROTOCOL"]  = "gsi"
    env.pop("X509_USER_CERT", None)
    env.pop("X509_USER_KEY",  None)
    return env


def _xrdfs_stat(path: str, proxy: str) -> int:
    """Return exit code of xrdfs stat on the VO server."""
    r = subprocess.run(
        ["xrdfs", VO_URL, "stat", path],
        env=_gsi_env(proxy), capture_output=True, timeout=10,
    )
    return r.returncode


def _xrdfs_ls(path: str, proxy: str) -> int:
    """Return exit code of xrdfs ls on the VO server."""
    r = subprocess.run(
        ["xrdfs", VO_URL, "ls", path],
        env=_gsi_env(proxy), capture_output=True, timeout=10,
    )
    return r.returncode


def _xrdcp_read(remote: str, proxy: str) -> int:
    """Download remote path to a temp file and return xrdcp exit code."""
    fd, tmp = tempfile.mkstemp()
    os.close(fd)
    try:
        r = subprocess.run(
            ["xrdcp", "-f", "-s", f"{VO_URL}/{remote}", tmp],
            env=_gsi_env(proxy), capture_output=True, timeout=15,
        )
        return r.returncode
    finally:
        if os.path.exists(tmp):
            os.unlink(tmp)


def _xrdcp_write(content: bytes, remote: str, proxy: str) -> int:
    """Upload *content* to remote path and return xrdcp exit code."""
    fd, tmp = tempfile.mkstemp()
    os.write(fd, content)
    os.close(fd)
    try:
        r = subprocess.run(
            ["xrdcp", "-f", "-s", tmp, f"{VO_URL}/{remote}"],
            env=_gsi_env(proxy), capture_output=True, timeout=15,
        )
        return r.returncode
    finally:
        os.unlink(tmp)


def _python_stat(path: str, proxy: str):
    """
    Return (status, stat_info) using the Python XRootD client.

    The XRootD client library caches credential state inside the process.  Run
    this small probe in a fresh interpreter so switching between VOMS proxies in
    the full pytest session uses the intended credential.
    """
    code = """
import json
import sys
from XRootD import client

fs = client.FileSystem(sys.argv[1])
status, _ = fs.stat(sys.argv[2])
print(json.dumps({"ok": bool(status.ok), "message": status.message}))
"""
    r = subprocess.run(
        [sys.executable, "-c", code, VO_URL, path],
        env=_gsi_env(proxy),
        capture_output=True,
        text=True,
        timeout=15,
    )
    if r.returncode != 0:
        message = (r.stderr or r.stdout).strip() or f"python stat exited {r.returncode}"
        return SimpleNamespace(ok=False, message=message), None

    payload = json.loads(r.stdout.strip().splitlines()[-1])
    return SimpleNamespace(ok=payload["ok"], message=payload["message"]), None


