def _voms_dn(pem: str, field: str) -> str:
    """Return subject or issuer DN from a PEM cert in OpenSSL one-line format."""
    r = subprocess.run(
        ["openssl", "x509", "-in", pem, "-noout", f"-{field}", "-nameopt", "compat"],
        check=True, capture_output=True, text=True,
    )
    # Output: "subject= /DC=..." or "issuer= /DC=..."
    return r.stdout.strip().split("=", 1)[1].strip()


def _make_voms_signing_cert():
    """
    Create a VOMS signing key + cert signed by the test CA, with
    SubjectKeyIdentifier so that voms-proxy-fake can embed an AKI in the AC.
    """
    os.makedirs(os.path.dirname(VOMS_CERT), exist_ok=True)

    if os.path.exists(VOMS_CERT) and os.path.exists(VOMS_KEY):
        return  # already exists

    # Generate key
    subprocess.run(
        ["openssl", "genrsa", "-out", VOMS_KEY, "2048"],
        check=True, capture_output=True,
    )

    # CSR
    csr = VOMS_CERT.replace(".pem", ".csr")
    subprocess.run(
        ["openssl", "req", "-new",
         "-key", VOMS_KEY,
         "-subj", "/DC=test/DC=xrootd/CN=voms.test.local",
         "-out", csr],
        check=True, capture_output=True,
    )

    # ext config
    ext_file = VOMS_CERT.replace(".pem", "_ext.conf")
    with open(ext_file, "w") as f:
        f.write("[voms_ext]\n"
                "subjectKeyIdentifier = hash\n"
                "authorityKeyIdentifier = keyid:always\n"
                "basicConstraints = CA:FALSE\n")

    # Sign with test CA
    subprocess.run(
        ["openssl", "x509", "-req",
         "-in", csr,
         "-CA", f"{CA_DIR}/ca.pem",
         "-CAkey", f"{CA_DIR}/ca.key",
         "-CAcreateserial",
         "-out", VOMS_CERT,
         "-days", "365",
         "-extensions", "voms_ext",
         "-extfile", ext_file],
        check=True, capture_output=True,
    )


def _make_vomsdir():
    """
    Create a vomsdir with LSC files for the 'cms' and 'atlas' VOs, both
    pointing at the local VOMS signing cert.
    """
    subject = _voms_dn(VOMS_CERT, "subject")
    issuer  = _voms_dn(VOMS_CERT, "issuer")
    lsc_content = f"{subject}\n{issuer}\n"

    for vo in ("cms", "atlas"):
        vo_dir = os.path.join(VOMSDIR, vo)
        os.makedirs(vo_dir, exist_ok=True)
        lsc = os.path.join(vo_dir, "voms.test.local.lsc")
        with open(lsc, "w") as f:
            f.write(lsc_content)


def _make_voms_proxy(vo: str, fqan: str, out: str):
    """Create a fake VOMS proxy for *vo* with the given *fqan*."""
    subprocess.run(
        VOMS_PROXY_FAKE + [
         "-cert",     USER_CERT,
         "-key",      USER_KEY,
         "-certdir",  CA_DIR,
         "-hostcert", VOMS_CERT,
         "-hostkey",  VOMS_KEY,
         "-voms",     vo,
         "-fqan",     fqan,
         "-uri",      "voms.test.local:15000",
         "-out",      out,
         "-hours",    "24"],
        check=True, capture_output=True,
    )


def _proxy_is_valid(path: str) -> bool:
    """Return True if the proxy file exists and is not yet expired."""
    if not os.path.exists(path):
        return False
    r = subprocess.run(
        ["openssl", "x509", "-in", path, "-noout", "-checkend", "3600"],
        capture_output=True,
    )
    return r.returncode == 0


