@pytest.fixture(scope="session", autouse=True)
def vo_nginx(tmp_path_factory):
    """
    Build the VOMS test infrastructure (signing cert, vomsdir, proxies) and
    start a dedicated nginx on VO_PORT with xrootd_require_vo restrictions.

    Skips the whole module if voms-proxy-fake is not on PATH or the nginx
    binary is absent.
    """
    if not VOMS_PROXY_FAKE:
        pytest.skip("voms-proxy-fake not found — skipping VO ACL tests")
    if not os.path.exists(NGINX_BIN):
        pytest.skip(
            f"nginx binary not found at {NGINX_BIN} — "
            "build with: cd /tmp/nginx-1.28.3 && make -j$(nproc)"
        )

    # ---- VOMS signing cert
    _make_voms_signing_cert()

    # ---- vomsdir (LSC files)
    _make_vomsdir()

    # ---- Plain GSI proxy (recreate if expired or absent)
    if not _proxy_is_valid(PROXY_STD):
        subprocess.run(
            ["python3", os.path.join(os.path.dirname(__file__), "..", "utils", "make_proxy.py")],
            check=True, capture_output=True,
        )

    # ---- VOMS proxies (recreate if expired or absent)
    if not _proxy_is_valid(PROXY_CMS):
        _make_voms_proxy("cms",   "/cms/Role=NULL/Capability=NULL",   PROXY_CMS)
    if not _proxy_is_valid(PROXY_ATLAS):
        _make_voms_proxy("atlas", "/atlas/Role=NULL/Capability=NULL", PROXY_ATLAS)

    # ---- Test data directories
    for subdir in ("cms", "atlas", "public"):
        path = os.path.join(DATA_ROOT, subdir)
        os.makedirs(path, exist_ok=True)
        # Seed a readable file in each directory
        seed = os.path.join(path, "seed.txt")
        if not os.path.exists(seed):
            with open(seed, "w") as f:
                f.write(f"seed file for {subdir}\n")

    # ---- Start VO nginx
    os.makedirs(VO_NGINX_DIR, exist_ok=True)
    conf_path = os.path.join(VO_NGINX_DIR, "nginx.conf")
    _write_vo_nginx_conf(conf_path)
    proc = _start_vo_nginx(conf_path)

    yield proc

    proc.terminate()
    try:
        proc.wait(timeout=5)
    except subprocess.TimeoutExpired:
        proc.kill()


